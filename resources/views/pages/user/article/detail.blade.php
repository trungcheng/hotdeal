                            <h1 class="title-head">Cách làm bánh bông lan cực dễ bằng nồi cơm điện</h1>
                            <div class="postby">
                                <span>Đăng bởi <b>Nguyễn Chánh Bảo Trung</b> vào lúc 01/11/2017</span>
                            </div>
                            <div class="article-details">
                                <div class="article-content">
                                    <div class="rte">
                                        <h2>Không cần phải có&nbsp;lò nướng, bạn cũng có thể làm bánh bông lan bằng nồi cơm điện. Chúng tôi&nbsp;sẽ mách bạn cách làm bánh bông lan thơm ngon, đơn giản và nhanh gọn bằng&nbsp;nồi cơm điện.</h2>
                                        <p><img alt="Làm bánh bông lan bằng nồi cơm điện" data-src="https://cdn1.tgdd.vn/Files/2014/07/11/553828/1.jpg" src="https://cdn1.tgdd.vn/Files/2014/07/11/553828/1.jpg" title="Làm bánh bông lan bằng nồi cơm điện" />&nbsp;</p>
                                        <h3>Nguyên liệu làm bánh bông lan bằng nồi cơm điện</h3>
                                        <p>- 60 g bột mì</p>
                                        <p>- 40 g bột bắp</p>
                                        <p>- 30 ml sữa tươi</p>
                                        <p>- 15 ml dầu ăn</p>
                                        <p>- 4 quả trứng gà lớn</p>
                                        <p>- 70 g đường</p>
                                        <p>- ½ muỗng cà phê muối</p>
                                        <p>- ½ muỗng cà phê vani</p>
                                        <p>- Chanh</p>
                                        <p><img alt="Cách làm bánh bông lan cực dễ bằng nồi cơm điện" src="//bizweb.dktcdn.net/100/270/860/files/lam-banh-bong-lan-bang-noi-com-dien-1.jpg?v=1510127995462" /></p>
                                        <h3>Dụng cụ</h3>
                                        <p>- Tô, que đánh trứng, cân, muỗng trộn</p>
                                        <p>- Nồi cơm điện (<strong>Lưu ý là&nbsp;nồi cơm điện nắp rời&nbsp;không làm bánh được</strong>)</p>
                                        <p>Bánh bông lan làm bằng nồi cơm điện có đặc điểm là mềm và xốp, lại tiết kiệm thời gian,&nbsp;<strong>thích hợp cho những gia đình chưa có&nbsp;lò nướng bánh</strong>.</p>
                                        <h3>Cách làm bánh bông lan bằng nồi cơm điện</h3>
                                        <p><strong>Bước 1:&nbsp;Rây bột, đánh bông lòng trắng trứng</strong></p>
                                        <p>Rây 60g bột mỳ và 40g bột bắp cho mịn.</p>
                                        <p>Tách riêng 4 lòng đỏ và lòng trắng trứng sang 2 tô riêng biệt.</p>
                                        <p>Cho nửa muỗng cà phê muối và 4, 5 giọt nước cốt chanh vào đánh đều với lòng trắng trứng. Vừa đánh vừa cho từ từ 70 g đường vào đánh đến khi lòng trắng trứng bông lên.</p>
                                        <p><img alt="Cách làm bánh bông lan cực dễ bằng nồi cơm điện" src="//bizweb.dktcdn.net/100/270/860/files/lam-banh-bong-lan-bang-noi-com-dien-2.jpg?v=1510128029301" /></p>
                                        <p><strong>Bước 2: Trộn lòng đỏ trứng</strong></p>
                                        <p>Cho vani, sữa tươi, và dầu ăn vào đánh đều với tô lòng đỏ trứng. Đổ bột đã rây vào và đánh đều để tạo thành hỗn hợp mịn.</p>
                                        <p><img alt="Cách làm bánh bông lan cực dễ bằng nồi cơm điện" src="//bizweb.dktcdn.net/100/270/860/files/lam-banh-bong-lan-bang-noi-com-dien-3.jpg?v=1510128048555" /></p>
                                        <p><strong>Bước 3:&nbsp;<strong>T</strong></strong><strong>rộn bột bánh</strong></p>
                                        <p>Trộn đều hỗn hợp lòng đỏ trứng với lòng trắng trứng đánh bông. Không trộn quá lâu sẽ làm bột bánh bị chai khi nướng.</p>
                                        <p><img alt="Cách làm bánh bông lan cực dễ bằng nồi cơm điện" src="//bizweb.dktcdn.net/100/270/860/files/lam-banh-bong-lan-bang-noi-com-dien-4.jpg?v=1510128062489" /></p>
                                        <p><strong>Bước 4: Nướng bánh</strong></p>
                                        <p>Lót giấy nến xuống đáy nồi cơm để khi bánh chín dễ lấy ra hơn. Đổ hỗn hợp bột bánh vào nồi cơm điện.</p>
                                        <p>- Nhấn&nbsp;Nấu - Cook&nbsp;đối với nồi cơm điện nắp gài và nồi cơm điện tử.</p>
                                        <p>- Nhấn Menu chọn chế độ nướng bánh và đợi nồi nấu khoảng 40 phút (đối với nồi cơm điện tử có chế độ nướng bánh).</p>
                                        <p><strong>Bước 5: Kiểm tra bánh chín chưa</strong></p>
                                        <p>Hết thời gian cài đặt, mở nắp nồi cơm và dùng ngón tay nhấn xuống mặt bánh. Nếu thấy mặt bánh bông lan không bị lõm xuống là bánh đã chín, chờ bánh nguội một chút là có thể lấy ra dùng ngay.</p>
                                        <p>Nếu bánh chưa chín, đậy nắp lại và nhấn nút Nấu - Cook thêm 1 lần nữa.</p>
                                        <h3>Thành phẩm</h3>
                                        <p><img alt="Cách làm bánh bông lan cực dễ bằng nồi cơm điện" src="//bizweb.dktcdn.net/100/270/860/files/lam-banh-bong-lan-bang-noi-com-dien-7.jpg?v=1510128087046" /></p>
                                        <p><i>Ổ bánh bông lan tròn đều, vàng ươm, thơm ngon</i></p>
